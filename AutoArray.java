class AutoArray{
	public static void main(String args[]){
		int month_days[] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
		// alternative array declaration, the above code can also be written as int[] month_days = {..........}
		// this alternative form of array declaration is useful while declaring several arrays, example: int[] arr1, arr2, arr3, arr4;
		System.out.println("November has "+month_days[10]+" days" );
	}	
		
}